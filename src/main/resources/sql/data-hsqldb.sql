CREATE TABLE modification
(
    id                  INTEGER PRIMARY KEY,
    title               VARCHAR(50)                                                       NOT NULL,
    generation_id       INTEGER                                                           NOT NULL,
    fuel_type           VARCHAR(16) check (fuel_type in ('DIESEL', 'GASOLINE', 'HYBRID')) NOT NULL,
    engine_type         VARCHAR(4) check (engine_type in
                                          ('L4', 'L5', 'L6', 'V6', 'V8', 'V10',
                                           'V12', 'W8',
                                           'W12'))                                        NOT NULL,
    engine_displacement integer                                                           NOT NULL,
    gearbox_type        VARCHAR(16) check (gearbox_type in
                                           ('AUTO', 'CVT', 'MANUAL', 'ROBOTIC'))          NOT NULL,
    wheeldrive_type     VARCHAR(4) check (wheeldrive_type in ('AWD', 'FWD', 'RWD'))       NOT NULL,
    body_style          VARCHAR(255)                                                      NOT NULL,
    acceleration        FLOAT                                                             NOT NULL,
    hp                  INTEGER                                                           NOT NULL,
    max_speed           INTEGER                                                           NOT NULL,
    CONSTRAINT fk__modification__generation FOREIGN KEY (generation_id)
        REFERENCES generation (id)
        MATCH SIMPLE
        ON DELETE RESTRICT
);

